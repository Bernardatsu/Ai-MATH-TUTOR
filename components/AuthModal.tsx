export const AuthModal: React.FC = () => {
  const { showAuthModal, setShowAuthModal, login } = useAuth();
  const [loadingProvider, setLoadingProvider] = useState<'google' | 'apple' | null>(null);

  if (!showAuthModal) return null;

  const handleLogin = async (provider: 'google' | 'apple') => {
    setLoadingProvider(provider);
    try {
      await login(provider);
    } catch (error) {
      console.error("Login failed", error);
    } finally {
      setLoadingProvider(null);
    }
  };

  return (
    <div className="fixed inset-0 z-[60] flex items-center justify-center p-4">
      {/* Backdrop */}
      <div 
        className="absolute inset-0 bg-slate-900/40 backdrop-blur-sm animate-fade-in"
        onClick={() => setShowAuthModal(false)}
      />

      {/* Modal Card */}
      <div className="relative w-full max-w-md bg-white rounded-3xl shadow-2xl p-8 animate-scale-in">
        <button
          onClick={() => setShowAuthModal(false)}
          className="absolute top-4 right-4 p-2 text-slate-400 hover:text-slate-600 hover:bg-slate-50 rounded-full transition-colors"
        >
          <X className="w-5 h-5" />
        </button>

        <div className="text-center mb-8">
          <div className="w-16 h-16 bg-indigo-50 rounded-2xl flex items-center justify-center mx-auto mb-4 text-indigo-600">
            <GraduationCap className="w-8 h-8" />
          </div>
          <h2 className="text-2xl font-bold text-slate-900">Welcome to Math Tutor</h2>
          <p className="text-slate-500 mt-2 text-sm">Sign in to save your history and access your solved problems from any device.</p>
        </div>

        <div className="space-y-4">
          <button
            onClick={() => handleLogin('google')}
            disabled={!!loadingProvider}
            className="w-full flex items-center justify-center gap-3 bg-white border border-slate-200 hover:bg-slate-50 text-slate-700 font-semibold py-3.5 px-4 rounded-xl transition-all hover:shadow-md active:scale-[0.98] disabled:opacity-70 disabled:cursor-not-allowed group relative overflow-hidden"
          >
            {loadingProvider === 'google' ? (
              <Loader2 className="w-5 h-5 animate-spin text-indigo-600" />
            ) : (
              <>
                <GoogleLogo />
                <span>Continue with Google</span>
              </>
            )}
          </button>

          <button
            onClick={() => handleLogin('apple')}
            disabled={!!loadingProvider}
            className="w-full flex items-center justify-center gap-3 bg-black hover:bg-slate-800 text-white font-semibold py-3.5 px-4 rounded-xl transition-all hover:shadow-lg active:scale-[0.98] disabled:opacity-70 disabled:cursor-not-allowed"
          >
            {loadingProvider === 'apple' ? (
              <Loader2 className="w-5 h-5 animate-spin text-white" />
            ) : (
              <>
                <Apple className="w-5 h-5 pb-0.5" fill="currentColor" />
                <span>Continue with Apple</span>
              </>
            )}
          </button>
        </div>

        <div className="mt-8 text-center">
            <p className="text-xs text-slate-400">
                By continuing, you agree to our <a href="#" className="underline hover:text-indigo-600">Terms of Service</a> and <a href="#" className="underline hover:text-indigo-600">Privacy Policy</a>.
            </p>
        </div>
      </div>
    </div>
  );
};